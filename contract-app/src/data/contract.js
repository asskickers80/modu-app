// "__밑줄__" 마커 → [{ text, u }] 세그먼트로 분해
export function toSegments(text) {
  return String(text)
    .split('__')
    .map((part, i) => ({ text: part, u: i % 2 === 1 }))
    .filter(seg => seg.text.length > 0)
}
